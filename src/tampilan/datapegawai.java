    protected void autonumber() {
    try {
        String sql = "SELECT id FROM datapegawai ORDER BY CAST(SUBSTRING(id, 4) AS UNSIGNED) DESC LIMIT 1";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        if (!rs.next()) {
            txtid.setText("IDP0001");
        } else {
            String id = rs.getString("id").substring(3); // Ambil angka setelah IDP
            int AN = Integer.parseInt(id) + 1;
            String Nol = "";

            if (AN < 10) {
                Nol = "000";
            } else if (AN < 100) {
                Nol = "00";
            } else if (AN < 1000) {
                Nol = "0";
            } else {
                Nol = ""; // Jika lebih dari 9999, langsung tempelkan angkanya
            }

            txtid.setText("IDP" + Nol + AN);
        }

        rs.close();
        st.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Auto Number Gagal: " + e);
    }
}

    
