    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        String jenis = null;
        if(rlaki.isSelected()){
            jenis = "Laki-Laki";
        }else if(rperempuan.isSelected()){
            jenis = "Perempuan";
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String fd = sdf.format(jtgl.getDate());
        String sql = "insert into datapegawai values (?,?,?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtid.getText());
            stat.setString(2, txtnama.getText());
            stat.setString(3, jenis);
            stat.setString(4, cbagama.getSelectedItem().toString());
            stat.setString(5, cbpendidikan.getSelectedItem().toString());
            stat.setString(6, txtnik.getText());
            stat.setString(7, txttlp.getText());
            stat.setString(8, cbjabatan.getSelectedItem().toString());
            stat.setString(9, fd);
            stat.setString(10, txtalamat.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            txtid.requestFocus();
            autonumber();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data gagal disimpan"+e);
        }
        datatable();
        kosong();
        aktif();
      
    }//GEN-LAST:event_bsimpanActionPerformed

